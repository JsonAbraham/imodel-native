/*---------------------------------------------------------------------------------**//**
* Tests the output from InitFromEPSGCode given provided EPSG code.
* @bsimethod
+---------------+---------------+---------------+---------------+---------------+------*/
TEST_F (SpecificEPSGTests, TestSpecificEPSGCodeInit)
    {
    GeoCoordinates::BaseGCSPtr currentGCS;
    Utf8String emptyString;

    currentGCS = GeoCoordinates::BaseGCS::CreateGCS();
    size_t number = s_ListOfEPSGTests.size();
    size_t index = 0;
    for (auto theStruct : s_ListOfEPSGTests)
        {
        index++;
        std::cerr << "Testing " << index << " of " << number << std::endl;  
        if (SUCCESS == currentGCS->InitFromEPSGCode(NULL, NULL, theStruct.m_epsgCode))
            {
            EXPECT_TRUE(currentGCS->IsValid());
            EXPECT_TRUE(Utf8String(currentGCS->GetName()) == theStruct.m_keyName);
            }
        else
            {
            EXPECT_TRUE(Utf8String(theStruct.m_keyName, false) == emptyString);
            }
        }
    }

    
